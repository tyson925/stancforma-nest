package com.stancforma.dxfReader.entities;

public class DL_FitPointData {
    /*! X coordinate of the fit point. */
    double x;
    /*! Y coordinate of the fit point. */
    double y;
    /*! Z coordinate of the fit point. */
    double z;

    public DL_FitPointData(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
